    private boolean validarCampos(){
    
    if(jTnomeFuncio.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o Nome");
        jTnomeFuncio.requestFocus(); //Chama o cursor 
        return false;
    }
    else if(jTApelido.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o Apelido");
       jTApelido.requestFocus();
       return false;
    }
    else if(jTBi.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o nr de BI");
        jTBi.requestFocus();
        return false;
    }
     else if(jTnuitFunc.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o nr de nuit");
        jTnuitFunc.requestFocus();
        return false;
    }
    
    if(jTdata.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite a data");
        jTdata.requestFocus();
        return false;
    }
    if(jTtelefoneFunc.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o nr de telefone");
        jTtelefoneFunc.requestFocus();
        return false;
    }
    if(jTemailFunc.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Digite o email");
        jTemailFunc.requestFocus();
        return false;
    }
    
    if(jComboFuncao.getSelectedItem().equals("")){
        JOptionPane.showMessageDialog(null, "Digite a função");
        jComboFuncao.requestFocus();
        return false;
    }
        return true;
    }

