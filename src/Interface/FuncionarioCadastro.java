    public boolean preencherCadastro() throws Exception{
       func.setNome(jTnomeFuncio.getText()); 
       func.setApelido(jTApelido.getText());
       func.setBi(jTBi.getText());
       func.setSexo(jRadioMasculino.getText()); //Converter em string, uma vez que ele é um botao de opcoes.
       func.setSexo(jRadioFemenino.getText());
       func.setNuit(Integer.parseInt(jTnuitFunc.getText())); //Convertendo Int para leitura 
       func.setNivelacad(jComboNivel.getSelectedItem().toString());
       func.setDataNasc(jTdata.getText());     
       func.setProvincia(jTprovFunc.getText());
       func.setDistrito(jTlocalidadFunc.getText());
       func.setSenha(jTsenha.getText());
       func.setUsuario(jTUser.getText());
       dep.setNomeDep(jCDepartamento.getSelectedItem().toString());
       func.setPerfil(jCcateg.getSelectedItem().toString());
       fuc.setNomeFunc(jComboFuncao.getSelectedItem().toString());
       conta.setTelefone(Integer.parseInt(jTtelefoneFunc.getText()));
       conta.setEmail(jTemailFunc.getText());
       //invocacao dos metodos que manipulam a gravacao
       futil.Gravar(func);
       contut.gravarCont(conta);
       fucut.gravarFuncao(fuc);
       deputil.gravarDep(dep);
       return true;
    }
